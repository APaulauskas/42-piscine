#include <stdlib.h>
#include "ft_list.h"

t_list	*ft_list_at(t_list *begin_list, unsigned int num)
{
	unsigned int i;

	i = 0;
	while (begin_list && i < num)
	{
		begin_list = begin_list->next;
		i++;
	}
	return (begin_list);
}
