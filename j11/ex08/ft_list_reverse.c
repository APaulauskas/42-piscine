#include <stdlib.h>
#include "ft_list.h"

void	ft_list_reverse(t_list **begin_list)
{
	t_list *next;
	t_list *previous;

	previous = 0;
	while (*begin_list)
	{
		next = (*begin_list)->next;
		(*begin_list)->next = previous;
		previous = *begin_list;
		*begin_list = next;
	}
	*begin_list = previous;
}
