void	swap(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int		ft_antidote(int i, int j, int k)
{
	if (i > j)
		swap(&i, &j);
	if (j > k)
		swap(&j, &k);
	if (i > j)
		swap(&i, &j);
	return (j);
}
