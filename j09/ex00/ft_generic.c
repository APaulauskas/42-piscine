#include <unistd.h>

void	print_char(char c)
{
	write(1, &c, 1);
}

void	print_string(char *string)
{
	int i;

	i = 0;
	while (string[i] != '\0')
	{
		print_char(string[i]);
		i++;
	}
}

void	ft_generic(void)
{
	print_string("Tut tut ; Tut tut\n");
}
