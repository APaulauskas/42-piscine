#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*result;

	i = 0;
	while (src[i] != '\0')
		i++;
	result = malloc(sizeof(char) * (i + 1));
	while (i >= 0)
	{
		result[i] = src[i];
		i--;
	}
	return (result);
}
