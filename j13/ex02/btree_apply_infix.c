#include <stdlib.h>
#include "ft_btree.h"

void	btree_apply_infix(t_btree *root, void (*appplyf)(void *))
{
	if (!root)
		return ;
	appplyf(root->item);
	if (root->left)
		btree_apply_infix(root->left, appplyf);
	if (root->right)
		btree_apply_infix(root->right, appplyf);
}
