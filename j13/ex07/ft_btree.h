#ifndef FT_BTREE_H
# define FT_BTREE_H

typedef struct	s_btree
{
	struct s_btree	*left;
	struct s_btree	*right;
	void			*item;
}				t_btree;

t_btree			*btree_create_node(void *item);

typedef struct	s_node
{
	struct s_node	*next;
	t_btree			*item;
	int				is_first;
	int				level;
}				t_node;

#endif
