int	ft_strncmp(char *s1, char *s2, int n)
{
	int i;

	i = 0;
	while (i < n || n < 0)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		if (s1[i] == '\0' || s2[i] == '\0')
			break ;
		i++;
	}
	return (0);
}
