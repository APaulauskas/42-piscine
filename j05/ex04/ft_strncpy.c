char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	if (src[i - 1] == '\0')
		dest[i - 1] = src[i - 1];
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
