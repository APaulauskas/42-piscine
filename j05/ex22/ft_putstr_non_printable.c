void	ft_putchar(char c);

void	ft_putnbr_base16(int nbr)
{
	char *base;

	*base = "0123456789abcdef";
	ft_putchar(base[nbr / 16]);
	ft_putchar(base[nbr % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] > 126)
		{
			ft_putchar('\\');
			ft_putnbr_base16(str[i]);
		}
		else
			ft_putchar(str[i]);
		i++;
	}
}
